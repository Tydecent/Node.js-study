console.log("Hello World!")                         // console.log()是一般的输出方法
console.error("ERROR:1+1 = 3")                      // error()是输出错误的方法，会被标红
console.warn("WARN:0.1+0.2=0.30000000000000004")    // warn()是输出警告的方法，也会被标红
console.info("INFO:Hello World")                    // info()是输出信息的方法

// console可以代表控制台，是一个内置、现成的工具，提供了多种对控制台操作的方式。